#include "primes.h"
#include <stdint.h>

uint8_t primes[] = {
	0b10101100,
	0b00101000,
	0b10001010,
	0b10100000,
	0b00100000,
	0b10001010,
	0b00100000,
	0b00101000,
	0b10001000,
	0b10000010,
	0b00001000,
	0b00000010,
	0b10100010,
	0b00101000,
	0b00000010,
	0b10000000,
	0b00001000,
	0b00001010,
	0b10100000,
	0b00100000,
	0b10001000,
	0b00100000,
	0b00101000,
	0b10000000,
	0b10100010,
	0b00000000,
	0b00001000,
	0b10000000,
	0b00101000,
	0b10000010,
	0b00000010,
	0b00001000,
};

bool is_prime(int num) {
	int index = num / 8;
	uint8_t mask = 1 << (num % 8);
	return (bool) (mask & primes[index]);
}
